/*
        Topic : Data types in Java
*/

public class JavaCode2 {

    public static void main(System args[])
    {   
        //for integer 
        byte b = 5;//require 1 byte
        short s = 5;// require 2 byte 
        int i = 5;//require 4 bytes = 32 bits for this 
        long l = 5l;//require 8 bytes at the end requires l

        double d = 5.5; //for floating
        float f = 6.6f;// also floating but required f at end   
        char c = 'O';// for character   
        String str = "ABC"; // for string

        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(d);
        System.out.println(f);
        System.out.println(c);
        System.out.println(str);

    } 
}
